// ==== Сборка content.json ====
function build() {
  const outGrades = grades.map((g) => {
    const sections = []
    for (const book of g.books) {
      book.sections.forEach((sec, si) => {
        sections.push({
          id: `${g.id}-${sanitize(book.course)}-${si}`,
          roman: sec.roman,
          name: sec.name,
          course: sec.course || '',
          topics: sec.topics.map((tp, ti) => ({
            id: `${g.id}-${sanitize(book.course)}-${si}-${ti}`,
            icon: tp.icon,
            title: tp.title,
            url: '',
          })),
        })
      })
    }
    return { id: g.id, title: g.title, subtitle: g.subtitle, sections }
  })

  return { settings: { adminUsername: 'admin', adminPasswordHash }, grades: outGrades }
}

function sanitize(str) {
  return (str || 'main').replace(/[^a-zа-я0-9]+/gi, '').slice(0, 8).toLowerCase() || 'main'
}

const content = build()
mkdirSync(dirname(OUT), { recursive: true })
writeFileSync(OUT, JSON.stringify(content, null, 2) + '\n', 'utf8')

const totals = content.grades.reduce(
  (acc, g) => {
    acc.sections += g.sections.length
    for (const s of g.sections) acc.topics += s.topics.length
    return acc
  },
  { sections: 0, topics: 0 },
)
